#define WHEELSPEED 100
#define ARMSPEED 30
#define LCLAWOPEN 155
#define LCLAWBOX 190
#define LCLAWBALL 255
#define RCLAWOPEN 100
#define RCLAWBOX 65
#define RCLAWBALL 0
